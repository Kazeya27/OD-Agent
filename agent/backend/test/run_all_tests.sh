#!/bin/bash
# 运行所有测试脚本

cd "$(dirname "$0")/.."

echo "================================================================================"
echo "运行所有测试"
echo "================================================================================"

# 检查测试数据库
if [ ! -f "geo_points.db" ]; then
    echo "⚠️  测试数据库不存在，正在生成..."
    python generate_test_db.py
    if [ $? -ne 0 ]; then
        echo "❌ 生成测试数据库失败"
        exit 1
    fi
fi

echo ""
echo "================================================================================"
echo "测试 1/4: 应用结构测试"
echo "================================================================================"
python test/test_refactored_app.py
TEST1=$?

echo ""
echo "================================================================================"
echo "测试 2/4: OD 路由接口测试"
echo "================================================================================"
python test/test_od_routes.py
TEST2=$?

echo ""
echo "================================================================================"
echo "测试 3/4: 分析函数测试（使用测试数据库）"
echo "================================================================================"
python test/test_with_test_db.py
TEST3=$?

echo ""
echo "================================================================================"
echo "测试 4/4: 分析函数单元测试"
echo "================================================================================"
if [ -f "test/test_analysis_functions.py" ]; then
    python test/test_analysis_functions.py
    TEST4=$?
else
    echo "⚠️  test_analysis_functions.py 不存在，跳过"
    TEST4=0
fi

echo ""
echo "================================================================================"
echo "测试总结"
echo "================================================================================"
echo "测试 1 - 应用结构测试: $([ $TEST1 -eq 0 ] && echo '✅ 通过' || echo '❌ 失败')"
echo "测试 2 - OD 路由测试: $([ $TEST2 -eq 0 ] && echo '✅ 通过' || echo '❌ 失败')"
echo "测试 3 - 分析函数测试: $([ $TEST3 -eq 0 ] && echo '✅ 通过' || echo '❌ 失败')"
echo "测试 4 - 单元测试: $([ $TEST4 -eq 0 ] && echo '✅ 通过' || echo '❌ 失败')"
echo "================================================================================"

# 计算总体结果
TOTAL=$((TEST1 + TEST2 + TEST3 + TEST4))
if [ $TOTAL -eq 0 ]; then
    echo "🎉 所有测试通过！"
    exit 0
else
    echo "⚠️  有测试失败，请检查输出"
    exit 1
fi

